package com.devis.beans;


public class Param {

  private String paramNature;
  private String paramCode;
  private String paramLibelle;
  private long idParam;


  public String getParamNature() {
    return paramNature;
  }

  public void setParamNature(String paramNature) {
    this.paramNature = paramNature;
  }


  public String getParamCode() {
    return paramCode;
  }

  public void setParamCode(String paramCode) {
    this.paramCode = paramCode;
  }


  public String getParamLibelle() {
    return paramLibelle;
  }

  public void setParamLibelle(String paramLibelle) {
    this.paramLibelle = paramLibelle;
  }


  public long getIdParam() {
    return idParam;
  }

  public void setIdParam(long idParam) {
    this.idParam = idParam;
  }

}
